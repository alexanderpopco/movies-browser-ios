import UIKit

extension UIImage {
    
    class func starFilledImage() -> UIImage? {
        return UIImage(named: "StarFilled")
    }
    
    class func starEmptyImage() -> UIImage? {
        return UIImage(named: "StarEmpty")
    }
}
